public class Dikdortgen {
    public static void main(String[] args) {
        double en = 4.5;
        double boy = 7.9;

        // Alan = en * boy
        double alan = en * boy;

        // Çevre = 2 * (en + boy)
        double cevre = 2 * (en + boy);

        System.out.println("Dikdörtgenin alanı: " + alan);
        System.out.println("Dikdörtgenin çevresi: " + cevre);
    }
}
